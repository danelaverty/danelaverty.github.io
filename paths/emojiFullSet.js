// Build search index function
export function buildSearchIndex() {
  const searchIndex = [];
  
  Object.entries(emojiCategories).forEach(([category, subcategories]) => {
    Object.entries(subcategories).forEach(([subcategory, emojis]) => {
      emojis.forEach(emojiObj => {
        searchIndex.push({
          ...emojiObj,
          searchText: [emojiObj.name, ...emojiObj.keywords].join(' ').toLowerCase(),
          category,
          subcategory
        });
      });
    });
  });
  
  return searchIndex;
}

// Simple search function
export function searchEmojis(query, searchIndex) {
  const lowerQuery = query.toLowerCase();
  return searchIndex.filter(emoji => 
    emoji.searchText.includes(lowerQuery)
  );
}
